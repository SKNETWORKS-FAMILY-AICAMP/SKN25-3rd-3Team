# ---------------------------------------------------------------------
# 4. 메인 시스템 프롬프트 조립
# [4차 변경] 출력 양식에 추정 칼로리 항목 추가
# ---------------------------------------------------------------------
def build_system_prompt(recipes: list, preferences: dict = None) -> str:
    """검색된 레시피 + 사용자 취향을 묶어 시스템 프롬프트 완성."""

    if not preferences:
        preferences = {
            "allergies": "없음",
            "difficulty": "상관없음",
            "cooking_time": "상관없음",
            "saved_sauces": "없음",
        }

    if not recipes:
        return "목록에 적합한 레시피가 없으면 '해당되는 레시피가 없습니다.'라고만 답해."

    prompt = (
        "당신은 냉장고 재료를 최대한 소진하도록 돕는 요리 도우미 '냉털봇'입니다.\n"
        "반드시 아래 제공된 [데이터베이스 검색 결과] 안에서만 요리를 선택하세요. "
        "목록에 없는 요리는 언급조차 하지 마세요.\n\n"
    )

    prompt += "[사용자 취향 및 환경 설정]\n"
    prompt += f"- 알레르기 유발 재료: {preferences.get('allergies', '없음')} (절대 추천 금지)\n"
    prompt += (
        f"- 선호 난이도/시간: {preferences.get('difficulty', '상관없음')} / "
        f"{preferences.get('cooking_time', '상관없음')} 이내\n"
    )
    prompt += f"- 현재 보유 중인 양념/소스: {preferences.get('saved_sauces', '없음')}\n\n"

    prompt += "### 데이터베이스 검색 결과 ###\n"
    for idx, recipe in enumerate(recipes, 1):
        title = recipe.get("title", "제목 없음")
        url = recipe.get("url", "링크 없음")
        ingredients = recipe.get("ingredients", [])
        steps = recipe.get("steps", [])

        if isinstance(ingredients, list):
            cleaned = [ing.replace(" 구매", "") for ing in ingredients]
            ing_str = ", ".join(cleaned)
        else:
            ing_str = ingredients

        prompt += f"[{idx}] 요리명: {title}\n"
        prompt += f" - 링크: {url}\n"
        prompt += f" - 재료: {ing_str}\n"
        prompt += " - 조리 순서:\n"
        if isinstance(steps, list):
            for step_num, step_text in enumerate(steps, 1):
                prompt += f"   {step_num}. {step_text}\n"
        else:
            prompt += f"   {steps}\n"
        prompt += "\n"

    # [4차 변경] 출력 양식에 추정 칼로리 추가
    prompt += (
        "### 엄격한 출력 지시사항 ###\n"
        "1. 친절하고 간결한 인사말과 함께 매칭 재료 수가 높은 순서로 2~3개를 추천해.\n"
        "2. 각 레시피마다 **반드시 아래의 양식을 그대로 복사해서** 내용을 채워넣어:\n\n"
        "🍳 **[요리명]**\n"
        "- **입력 재료**: (사용자가 질문한 식재료 + 보유 중인 소스 중 사용된 것)\n"
        "- **필요한 재료**: (이 레시피를 만들기 위해 추가로 필요한 재료)\n"
        "- **조리법 설명**: (검색된 조리 순서를 바탕으로 핵심만 3~5단계로 간략히 요약)\n"
        "- **추정 칼로리**: 약 OOOkcal (1인분 기준, LLM 추정)\n"
        "- **참고 링크**: (제공된 URL)\n\n"
        "3. 조리법 설명은 원문을 그대로 길게 나열하지 말고, 초보자도 알기 쉽게 요약해.\n"
        "4. 칼로리는 일반적인 1인분(약 300g 기준)을 가정하여 추정하되, "
        "(LLM 추정)이라는 표기를 절대 빼지 마.\n"
    )

    return prompt
